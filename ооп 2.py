class math:
    def __init__(self,a,b):
         self.a, self.b = a,b
    def addition(self):
         print( self.a+self.b)
    def multiplication(self):
         print( self.a*self.b)
    def division(self):
         print( self.a/self.b)
    def subtraction(self):
         print(self.a - self.b)

